public class TROCIO_ACTIVITY_I{
    public static void main(String[]args){
        int x = 10;
        int y = 20;
        int z = 15;

        System.out.println(x*y-z);
        System.out.println(x+y+z);
        System.out.println(x-y-z);
        System.out.println(x*y*z);
        System.out.println(x/y/z);
    }
}